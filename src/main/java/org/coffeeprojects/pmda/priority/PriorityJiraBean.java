package org.coffeeprojects.pmda.priority;

public class PriorityJiraBean {

    private String id;

    private String name;

    public String getId() {
        return id;
    }

    public PriorityJiraBean setId(String id) {
        this.id = id;
        return this;
    }

    public String getName() {
        return name;
    }

    public PriorityJiraBean setName(String name) {
        this.name = name;
        return this;
    }
}
